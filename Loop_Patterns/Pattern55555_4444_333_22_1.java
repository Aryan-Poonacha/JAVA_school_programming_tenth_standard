package Loop_Patterns;
class Pattern55555_4444_333_22_1
{
    public static void main(String args[])
    {
        int x = 0;
        for(int i = 5; i>=1; i--)
        {
            x = i;
            for(int j=i; j>=1; j--)
            {
                System.out.print(x+ " ");
            }
            System.out.println();
        }
    }
}
