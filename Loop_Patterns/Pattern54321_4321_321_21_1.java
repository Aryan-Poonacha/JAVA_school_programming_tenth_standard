package Loop_Patterns;
class Pattern54321_4321_321_21_1
{
    public static void main(String args[])
    {
        for(int i=5; i>=1; i--)
        {
            for(int j=i; j>=1; j--)
            {
                System.out.print(j+ " ");
            }
            System.out.println();
        }
    }
}
